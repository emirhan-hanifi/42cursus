#include "libftprintf.h"

int	ft_printf(const char *format, ...)
{
	va_list	args;
	int		count;

	va_start(args, format);
	count = 0;
	while (*format)
	{
		if (*format == '%')
		{
			format++;
			count += ft_print_format(args, *format);
		}
		else
		{
			count += ft_putchr(*format);
		}
		format++;
	}
	va_end(args);
	return (count);
}